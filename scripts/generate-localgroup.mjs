// Compacts McConnachie's Local Group census (2012, AJ 144, 4 — VizieR
// J/AJ/144/4) into src/data/localgroup.ts: every known dwarf galaxy in
// and around the Local Group with a measured position, distance, size,
// and luminosity. The Milky Way, LMC, SMC (rendered from stars), and
// M31/M33 (Messier destinations) are excluded as already present.
//
// Input: TSV from
//   https://vizier.cds.unistra.fr/viz-bin/asu-tsv?-source=J/AJ/144/4/catalog
//     &-out.max=200&-out=Name,RAJ2000,DEJ2000,D,R1,VMag,SubG
//
// Usage: node scripts/generate-localgroup.mjs <catalog.tsv>

import { readFileSync, writeFileSync } from 'node:fs';

const SKIP = new Set(['The Galaxy', 'LMC', 'SMC', 'Andromeda', 'Triangulum', 'Canis Major']);

const lines = readFileSync(process.argv[2], 'utf8').split('\n');
const rows = [];
let started = 0;
for (const line of lines) {
  if (line.startsWith('#') || !line.trim()) continue;
  if (started < 3) {
    started++; // header, units, dashes
    continue;
  }
  const f = line.split('\t').map((x) => x.trim());
  const [name, ra, dec, d, r1, vmag] = [f[0], f[1], f[2], parseFloat(f[3]), parseFloat(f[4]), parseFloat(f[5])];
  if (!name || SKIP.has(name) || !Number.isFinite(d)) continue;
  const [h, m, s] = ra.split(/\s+/).map(parseFloat);
  const raDeg = (h + m / 60 + (s || 0) / 3600) * 15;
  const dm = dec.split(/\s+/).map(parseFloat);
  const sign = dec.trim().startsWith('-') ? -1 : 1;
  const decDeg = sign * (Math.abs(dm[0]) + (dm[1] || 0) / 60 + (dm[2] || 0) / 3600);
  // Physical half-light radius from the angular one; a 0.15 kpc floor
  // keeps the ultra-faints visible as more than a bare pixel.
  const rKpc = Number.isFinite(r1) ? Math.max(d * Math.tan((r1 / 60 / 180) * Math.PI), 0.15) : 0.3;
  rows.push([name, raDeg, decDeg, d, rKpc, Number.isFinite(vmag) ? vmag : -6]);
}

let failures = 0;
const check = (label, ok) => {
  if (!ok) failures++;
  console.log(`${ok ? '  ok ' : 'FAIL '} ${label}`);
};
check(`${rows.length} dwarfs (expect 90–110)`, rows.length >= 90 && rows.length <= 110);
const draco = rows.find((r) => r[0] === 'Draco');
check(`Draco at ${draco?.[3]} kpc (expect 74–78)`, !!draco && draco[3] > 74 && draco[3] < 78);
const fornax = rows.find((r) => r[0] === 'Fornax');
check('Fornax present', !!fornax);
if (failures) {
  console.error('not writing');
  process.exit(1);
}

const body = rows
  .map(
    ([n, ra, dec, d, r, v]) =>
      `  ['${n.replace(/'/g, "\\'")}', ${ra.toFixed(4)}, ${dec.toFixed(4)}, ${d}, ${r.toFixed(3)}, ${v}],`,
  )
  .join('\n');
writeFileSync(
  'src/data/localgroup.ts',
  `// GENERATED by scripts/generate-localgroup.mjs — do not edit.
// The Local Group census (McConnachie 2012, AJ 144, 4): every known
// dwarf galaxy with measured position, distance (kpc), half-light
// radius (kpc), and absolute V magnitude.
// [name, ra°, dec°, distKpc, rHalfKpc, VMag]
export const LOCAL_GROUP: [string, number, number, number, number, number][] = [
${body}
];
`,
);
console.log(`wrote src/data/localgroup.ts: ${rows.length} dwarfs`);
