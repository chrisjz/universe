// Bakes the 88 IAU constellations (western sky culture) into
// src/data/constellations.ts: line segments and label anchors as J2000
// RA/Dec, rotated into the scene at load by sky.ts.
//
// Data: d3-celestial by Olaf Frohn (BSD-3-Clause),
//   curl -sLO https://raw.githubusercontent.com/ofrohn/d3-celestial/master/data/constellations.lines.json
//   curl -sLO https://raw.githubusercontent.com/ofrohn/d3-celestial/master/data/constellations.json
//   node scripts/generate-constellations.mjs constellations.lines.json constellations.json

import { readFileSync, writeFileSync } from 'node:fs';

const lines = JSON.parse(readFileSync(process.argv[2], 'utf8'));
const names = JSON.parse(readFileSync(process.argv[3], 'utf8'));

const r = (v) => Number(v.toFixed(2));
const segs = [];
for (const f of lines.features) {
  for (const line of f.geometry.coordinates) {
    for (let i = 0; i + 1 < line.length; i++) {
      segs.push(r(line[i][0]), r(line[i][1]), r(line[i + 1][0]), r(line[i + 1][1]));
    }
  }
}
const labels = names.features.map((f) => [r(f.geometry.coordinates[0]), r(f.geometry.coordinates[1]), f.properties.name]);

writeFileSync(
  'src/data/constellations.ts',
  `// Generated by scripts/generate-constellations.mjs — do not edit by hand.
// The 88 IAU constellations (western sky culture), from d3-celestial by
// Olaf Frohn (BSD-3-Clause). RA/Dec in J2000 degrees.

// Line segments: ra0, dec0, ra1, dec1 per segment.
export const CONSTELLATION_SEGMENTS: number[] = [
${JSON.stringify(segs).slice(1, -1)},
];

// Label anchors: [ra, dec, name].
export const CONSTELLATION_LABELS: [number, number, string][] = [
${labels.map((l) => `  ${JSON.stringify(l)},`).join('\n')}
];
`,
);
console.log(`wrote ${segs.length / 4} segments, ${labels.length} labels`);
