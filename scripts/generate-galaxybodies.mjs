// Fetches each Messier galaxy's measured orientation from RC3 (the Third
// Reference Catalogue of Bright Galaxies, VizieR VII/155) and emits
// src/data/galaxybodies.ts: position angle on the sky, axis ratio (which
// carries the inclination for disks and the projected flattening for
// ellipticals), and the numeric Hubble stage T.
//
// The bodies themselves are built in scene.ts: particle impressions on
// these REAL dimensions and orientations — the same honesty class as the
// atlas's own Milky Way (real size, illustrative arm pattern).
//
// Usage: node scripts/generate-galaxybodies.mjs   (network: VizieR)

import { writeFileSync } from 'node:fs';
import { execFileSync } from 'node:child_process';

// The Messier list ships in src/data/messier.ts; import via esbuild strip.
execFileSync('npx', ['esbuild', 'src/data/messier.ts', '--outfile=.messier-tmp.mjs', '--format=esm']);
const { MESSIER } = await import('../.messier-tmp.mjs');
execFileSync('rm', ['.messier-tmp.mjs']);

const galaxies = MESSIER.filter(([, , , , , , type]) => type === 's' || type === 'e');
console.log(`${galaxies.length} Messier galaxies (type s/e)`);

const rows = [];
for (const [m, name, ra, dec] of galaxies) {
  const ra360 = ((ra % 360) + 360) % 360;
  const url = `https://vizier.cds.unistra.fr/viz-bin/asu-tsv?-source=VII/155/rc3&-c=${ra360.toFixed(4)}${dec >= 0 ? '%2B' : ''}${dec.toFixed(4)}&-c.rs=600&-out=PA,R25,T&-sort=_r&-out.max=1`;
  let text = '';
  for (let a = 0; a < 4; a++) {
    try {
      text = execFileSync('curl', ['-s', '--max-time', '60', url], { encoding: 'utf8' });
      if (text.includes('---')) break;
    } catch {
      /* retry */
    }
    execFileSync('sleep', ['3']);
  }
  const data = text.split('\n').filter((l) => l && !l.startsWith('#'));
  const row = data[3]; // header, units, dashes, first row
  if (!row) {
    console.log(`  M${m} (${name || 'unnamed'}): no RC3 match — skipped`);
    continue;
  }
  const [paS, r25S, tS] = row.split('\t').map((x) => x.trim());
  const pa = parseFloat(paS);
  const r25 = parseFloat(r25S);
  const t = parseFloat(tS);
  rows.push([
    m,
    Number.isFinite(pa) ? pa : 0,
    Number.isFinite(r25) ? Math.pow(10, r25) : 1,
    Number.isFinite(t) ? t : 0,
  ]);
  execFileSync('sleep', ['1']); // polite pacing
}

let failures = 0;
const check = (label, ok) => {
  if (!ok) failures++;
  console.log(`${ok ? '  ok ' : 'FAIL '} ${label}`);
};
const get = (m) => rows.find((r) => r[0] === m);
check(`${rows.length} galaxies resolved (expect ≥ 34)`, rows.length >= 34);
const m31 = get(31);
check(
  `M31 PA ${m31?.[1]}° (published 35–38), ratio ${m31?.[2].toFixed(2)} (>2.5), T ${m31?.[3]}`,
  !!m31 && m31[1] > 25 && m31[1] < 45 && m31[2] > 2.5 && m31[3] >= 2 && m31[3] <= 4,
);
const m33 = get(33);
check(`M33 T ${m33?.[3]} (published ~6, late spiral)`, !!m33 && m33[3] >= 5 && m33[3] <= 7);
const m87 = get(87);
check(`M87 T ${m87?.[3]} (elliptical, ≤ −3)`, !!m87 && m87[3] <= -3);
if (failures) {
  console.error('not writing');
  process.exit(1);
}

writeFileSync(
  'src/data/galaxybodies.ts',
  `// GENERATED by scripts/generate-galaxybodies.mjs — do not edit.
// Measured orientations for the Messier galaxies, from RC3 (de
// Vaucouleurs et al. 1991, VizieR VII/155):
// [M number, position angle ° (N→E), D25/d25 axis ratio, Hubble stage T]
export const GALAXY_BODIES: [number, number, number, number][] = [
${rows.map((r) => `  [${r[0]}, ${r[1]}, ${r[2].toFixed(3)}, ${r[3]}],`).join('\n')}
];
`,
);
console.log(`wrote src/data/galaxybodies.ts: ${rows.length} galaxies`);
